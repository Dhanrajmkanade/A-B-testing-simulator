# --- Re-Perform Two-Proportion Z-Test (from Phase 3) ---
n_control = summary_stats.loc[summary_stats['group'] == 'Control', 'total_users'].iloc[0]
x_control = summary_stats.loc[summary_stats['group'] == 'Control', 'total_conversions'].iloc[0]
n_treatment = summary_stats.loc[summary_stats['group'] == 'Treatment', 'total_users'].iloc[0]
x_treatment = summary_stats.loc[summary_stats['group'] == 'Treatment', 'total_conversions'].iloc[0]
z_score, p_value = sm.stats.proportions_ztest([x_control, x_treatment], [n_control, n_treatment])

# Set a style for the plots
sns.set_style("whitegrid")

# Create a bar chart of conversion rates
plt.figure(figsize=(8, 6))
sns.barplot(x='group', y='conversion_rate', data=conversion_rates, hue='group', palette='viridis', legend=False)

plt.title('Conversion Rates by Group (Control vs. Treatment)')
plt.xlabel('Group')
